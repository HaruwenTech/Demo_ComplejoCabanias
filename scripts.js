    // Función para inicializar el carrusel en móviles
    function initMobileCarousel() {
        const carousel = document.querySelector('#galleryModal .carousel');
        const carouselItems = carousel.querySelectorAll('.carousel-item');
        const prevButton = carousel.querySelector('.carousel-control-prev');
        const nextButton = carousel.querySelector('.carousel-control-next');
        let currentIndex = 0;
        // Asegurarse de que solo un elemento esté activo
        function updateCarousel() {
            carouselItems.forEach((item, index) => {
                item.classList.remove('active');
                if (index === currentIndex) {
                    item.classList.add('active');
                }
            });
        }
        // Ir a la imagen anterior
        prevButton.addEventListener('click', () => {
            currentIndex = (currentIndex - 1 + carouselItems.length) % carouselItems.length;
            updateCarousel();
        });
        // Ir a la imagen siguiente
        nextButton.addEventListener('click', () => {
            currentIndex = (currentIndex + 1) % carouselItems.length;
            updateCarousel();
        });
        // Inicializar el primer elemento como activo
        updateCarousel();
    }
    // Llamar a la función cuando se abre el modal
    document.getElementById('galleryModal').addEventListener('shown.bs.modal', initMobileCarousel);
